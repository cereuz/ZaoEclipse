package com.onezao.onezao.zhihurss1129;

public class ZhihuChannelBean {
          public String  channel;
          public String  title;
          public String  link;
          public String  description;
          public String  atomLink;
          public String  language;
          public String  copyright;
          public String  lastBuildDate;
          public String  ttl;
		public String getChannel() {
			return channel;
		}
		public void setChannel(String channel) {
			this.channel = channel;
		}
		public String getTitle() {
			return title;
		}
		public void setTitle(String title) {
			this.title = title;
		}
		public String getLink() {
			return link;
		}
		public void setLink(String link) {
			this.link = link;
		}
		public String getDescription() {
			return description;
		}
		public void setDescription(String description) {
			this.description = description;
		}
		public String getAtomLink() {
			return atomLink;
		}
		public void setAtomLink(String atomLink) {
			this.atomLink = atomLink;
		}
		public String getLanguage() {
			return language;
		}
		public void setLanguage(String language) {
			this.language = language;
		}
		public String getCopyright() {
			return copyright;
		}
		public void setCopyright(String copyright) {
			this.copyright = copyright;
		}
		public String getLastBuildDate() {
			return lastBuildDate;
		}
		public void setLastBuildDate(String lastBuildDate) {
			this.lastBuildDate = lastBuildDate;
		}
		public String getTtl() {
			return ttl;
		}
		public void setTtl(String ttl) {
			this.ttl = ttl;
		}
		@Override
		public String toString() {
			return "ZhihuChannelBean [channel=" + channel + ", title=" + title
					+ ", link=" + link + ", description=" + description
					+ ", atomLink=" + atomLink + ", language=" + language
					+ ", copyright=" + copyright + ", lastBuildDate="
					+ lastBuildDate + ", ttl=" + ttl + "]";
		}
          
          
          
}
